<template>
  <div id="app" v-loading="pageLoadingTF">
    <div>
      <input
        type="file"
        :disabled="status !== Status.wait"
        @change="handleFileChange"
      />
      <el-button @click="handleUpload" :disabled="uploadDisabled"
        >上传</el-button
      >
      <el-button @click="handleResume" v-if="status === Status.pause"
        >恢复</el-button
      >
      <el-button
        v-else
        :disabled="status !== Status.uploading || !container.hash"
        @click="handlePause"
        >暂停</el-button
      >
    </div>
    <div>
      <div>计算文件 hash:{{ hashTxt }}</div>
      <el-progress :percentage="hashPercentage"></el-progress>
      <div>总进度</div>
      <el-progress :percentage="fakeUploadPercentage"></el-progress>
    </div>
    <div style="width:200px" v-if="tableTF">
      <div v-for="(ielem, indOne) in data" :key="indOne" class="cube">
        <div
          :class="{
            uploading: ielem.percentage > 0 && ielem.percentage < 100,
            success: ielem.percentage == 100,
            error: ielem.percentage < 0
          }"
          :style="{ height: ielem.percentage + '%' }"
        >
          <i
            v-if="ielem.percentage < 100 && ielem.percentage > 1"
            class="el-icon-loading"
            style="color:#F56C6C;"
          ></i>
        </div>
      </div>
    </div>
